#alien game if-else statement
color = ("Green","Yellow","Red")

#running if block
alien_color = (color[0])
if (alien_color == (color[0])):
    print ("You get 5 points!")
else:
    print ("You get 10 points!")

#running else block
alien_color = (color[1])
if (alien_color == (color[0])):
    print ("You get 5 points!")
else:
    print ("You get 10 points!")
